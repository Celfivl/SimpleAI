
def au_poivre_recipe():
    recipe = """
Au Poivre Sauce Recipe:

Ingredients:
- 2 tablespoons whole black peppercorns, coarsely crushed
- 2 tablespoons butter
- 1/4 cup finely chopped shallots
- 1/2 cup beef broth
- 1/4 cup heavy cream
- 2 tablespoons Cognac or brandy (optional)
- 1 teaspoon Dijon mustard
- Salt to taste

Instructions:
1. Coarsely crush the black peppercorns using a mortar and pestle or by placing them in a plastic bag and crushing them with a rolling pin.
2. Melt the butter in a saucepan over medium heat. Add the crushed peppercorns and shallots and cook for about 2 minutes, until fragrant.
3. If using, add the Cognac or brandy and carefully ignite it. Let the alcohol burn off.
4. Pour in the beef broth and bring to a simmer. Cook until the sauce has reduced slightly, about 5 minutes.
5. Stir in the heavy cream and Dijon mustard. Season with salt to taste.
6. Simmer for another 2-3 minutes, until the sauce has thickened to your desired consistency.
7. Serve immediately over your favorite steak or other grilled meats.
"""
    return recipe

print(au_poivre_recipe())
